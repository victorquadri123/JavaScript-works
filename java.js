// Summary
// Scope:
// var is function-scoped.
// let and const are block-scoped.

// Hoisting:
// var is hoisted and initialized with undefined.
// let and const are hoisted but not initialized, resulting in a ReferenceError if accessed before declaration.

// Re-declaration:
// var allows re-declaration within the same scope.
// let and const do not allow re-declaration within the same scope.

// Re-assignment:
// var and let allow re-assignment.
// const does not allow re-assignment and requires an initializer.


